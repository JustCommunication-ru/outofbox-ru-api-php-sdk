<?php

namespace Outofbox\OutofboxSDK\Model;

class WarehouseStore
{
    /**
     * @var int
     */
    protected $id;

    /**
     * @var string
     */
    protected $name;

    /**
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * @param int $id
     * @return WarehouseStore
     */
    public function setId($id)
    {
        $this->id = $id;
        return $this;
    }

    /**
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * @param string $name
     * @return WarehouseStore
     */
    public function setName($name)
    {
        $this->name = $name;
        return $this;
    }
}
